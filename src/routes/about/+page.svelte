				>
				under the auspices of the
				<a
					class="font-bold hover:underline"
					href="https://www.berlin-university-alliance.de/index.html"
					target="_blank"
					rel="noreferrer">Berlin University Alliance</a
				>
				and the
				<a class="font-bold hover:underline" href="https://www.fu-berlin.de/"
					>Freie Universität Berlin</a
				>, which aimed to improve the agency and visibility of NLS-related research in
				the Digital Humanities in Germany. One part of the project was to get an idea of
				the state of the field of “Digitale Arabistik” in Germany, mainly in terms of
				completed, ongoing, and future research projects, but also in terms of available
				infrastructure and relevant initiatives in the field.
			</p>

			<p>
				As we began gathering data on digital projects dealing with Arabic or similar
				languages, we thought about how to provide this data in a way that commits to
				OpenScience principles. So we chose a public Git repository as our main data
				store, offering the data as JSON in a way that should be as straightforward as
				possible. Everyone who is interested should be able to contribute without having
				to deal with too much of a technology stack.
			</p>

			<p>
				For those who are not able or willing to work with JSON, we developed a
				graphical user interface—this website—which offers not only a list of all
				projects for which we have collected data, but also small visualizations (on
				which we continue to iterate). For an easier way to provide data than writing
				plain JSON, we offer a web form that can generate a file conforming to our
				schema. All of these features will be improved over time, depending on the needs
				and demands of the community.
			</p>
